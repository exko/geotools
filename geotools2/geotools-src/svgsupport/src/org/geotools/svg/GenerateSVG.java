package org.geotools.svg;
//import org.apache.batik.dom.GenericDOMImplementation; 
/**
 * @version $Id: GenerateSVG.java,v 1.5 2002/10/28 16:55:44 ianturton Exp $
 * @author James Macgill, CCG
 */
public class GenerateSVG {
    
    private static java.util.logging.Logger LOGGER = java.util.logging.Logger.getLogger("org.geotools.svgsupport");
    
    /** Creates a new instance of GenerateSVG. */
    public GenerateSVG() {
    }
    
    public void go(org.geotools.map.Map map, com.vividsolutions.jts.geom.Envelope env, java.io.OutputStream out) throws java.io.IOException{
        // Get a DOMImplementation
//        DOMImplementation domImpl =
//        GenericDOMImplementation.getDOMImplementation();
        org.w3c.dom.Document document = null;
        try{
            javax.xml.parsers.DocumentBuilderFactory dbf = javax.xml.parsers.DocumentBuilderFactory.newInstance();
            javax.xml.parsers.DocumentBuilder db = dbf.newDocumentBuilder();

            // Create an instance of org.w3c.dom.Document
            document = db.getDOMImplementation().createDocument(null, "svg", null);
        }catch (javax.xml.parsers.ParserConfigurationException pce){
            LOGGER.severe("Unable to get new dom document " + pce);
            return;
        }
        // Set up the context
        org.apache.batik.svggen.SVGGeneratorContext ctx = org.apache.batik.svggen.SVGGeneratorContext.createDefault(document);
        ctx.setComment("Generated by GeoTools2 with Batik SVG Generator");
        
        // Create an instance of the SVG Generator
        org.apache.batik.svggen.SVGGraphics2D g2d = new org.apache.batik.svggen.SVGGraphics2D(ctx, true); 
        g2d.setSVGCanvasSize(new java.awt.Dimension(300, 300));
        
        org.geotools.renderer.Java2DRenderer renderer = new org.geotools.renderer.Java2DRenderer();
        renderer.setOutput(g2d, new java.awt.Rectangle(g2d.getSVGCanvasSize()));
        LOGGER.finest("rendering map");
        map.render(renderer, env);
        LOGGER.finest("writing to file");
        g2d.stream(new java.io.OutputStreamWriter(out,"UTF-8"));
        
    }
    
}
