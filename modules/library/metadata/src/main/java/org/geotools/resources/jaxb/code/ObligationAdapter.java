package org.geotools.resources.jaxb.code;

import javax.xml.bind.annotation.adapters.XmlAdapter;
import org.opengis.annotation.Obligation;


/**
 * JAXB adapter for {@link Obligation}, in order to integrate the value in a tags
 * respecting the ISO-19139 standard. See package documentation to have more information
 * about the handling of CodeList in ISO-19139.
 *
 * @since 2.5
 * @source $URL$
 * @author Cédric Briançon
 */
public class ObligationAdapter extends XmlAdapter<String, Obligation> {

    public Obligation unmarshal(String v) throws Exception {
        return Obligation.valueOf(v);
    }

    public String marshal(Obligation v) throws Exception {
        if (v == null) {
            return (String)null;
        }
        return v.name();
    }
}
